fun main() {
    var number = 12
    number += 5   // number = number+5
    println("number  = $number")

    number = 12
    number -= 5   // number = number-5
    println("number  = $number")

    number = 12
    number *= 5   // number = number*5
    println("number  = $number")

    var number2 = 12
    number2 /= 5   // number = number/5
    println("number  = $number2")

    number = 12
    number %= 5   // number = number%5
    println("number  = $number")

}